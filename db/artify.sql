
-- CUSTOMER TABLE
CREATE TABLE IF NOT EXISTS CUSTOMER(
    CUSTOMER_ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    CUSTOMER_NAME VARCHAR(100) NOT NULL, 
    CUSTOMER_EMAIL VARCHAR(100) NOT NULL,
    CUSTOMER_PASSWORD VARCHAR(20) NOT NULL,
    CUSTOMER_ADDRESS VARCHAR(100) NOT NULL,
    CUSTOMER_TEL_NUM INT(10) NOT NULL
);

-- ARTWORKS TABLE
CREATE TABLE IF NOT EXISTS ARTWORKS(
    ARTWORK_ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    ARTWORK_NAME VARCHAR(100) NOT NULL, 
    ARTWORK_DIMENSIONS VARCHAR(100) NOT NULL, 
    ARTWORK_DESC VARCHAR(5000) DEFAULT NULL, 
    ARTWORK_PRICE DECIMAL(10, 2) NOT NULL, 
    ARTIST_NAME VARCHAR(30) NOT NULL,
    ARTWORK_URL VARCHAR(255) NOT NULL,
    STOCK INT(8) NOT NULL,
    CATEGORY_ID INT(11) NOT NULL,
    FOREIGN KEY (CATEOGRY_ID) REFERENCES CATEGORY(CATEGORY_ID) 
);



-- CART TABLE
CREATE TABLE IF NOT EXISTS CART(
    CART_ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    CUSTOMER_ID INT(11) NOT NULL,
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID) 
);

-- ORDERS TABLE
CREATE TABLE IF NOT EXISTS ORDERS(
    ORDERS_ID INT(5) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    ORDERS_AMOUNT DECIMAL(5,2) NOT NULL,
    CUSTOMER_ID INT(11) NOT NULL,
    ORDER_DATE DATETIME NOT NULL, 
    TOTAL_AMOUNT DECIMAL(10,2) NOT NULL,
    SHIPPING_STATUS ENUM("Pending", "Successfully Shipped"),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID) 
);

-- ORDER_DETAILS TABLE
CREATE TABLE IF NOT EXISTS ORDERS_TABLE(
    ORDER_DETAILS_ID INT(5) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    QUANTITY INT(100) NOT NULL,
    PRICE DECIMAL(10,2) NOT NULL,
    ORDERS_ID INT(5),
    ARTWORK_ID INT(5),
    FOREIGN KEY (ORDERS_ID) REFERENCES ORDERS(ORDERS_ID),
    FOREIGN KEY (ARTWORK_ID) REFERENCES ARTWORKS(ARTWORK_ID) 
);

-- PAYMENT TABLE
CREATE TABLE IF NOT EXISTS PAYMENT(
    PAYMENT_ID INT(5) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    PAYMENT_METHOD VARCHAR(50) NOT NULL,
    CUSTOMER_ID INT(11) NOT NULL,
    ORDERS_ID INT(5),
    FOREIGN KEY (ORDERS_ID) REFERENCES ORDERS(ORDERS_ID),
    FOREIGN KEY (CUSTOMER_ID) REFERENCES CUSTOMER(CUSTOMER_ID) 
);

-- ADMIN TABLE 
CREATE TABLE IF NOT EXISTS ADMINS(
    ADMIN_ID INT(5) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    ADMIN_NAME VARCHAR(255) NOT NULL,
    ADMIN_PASSWORD VARCHAR(20) NOT NULL
);

-- CATEGORY TABLE (ADMIN)
CREATE TABLE IF NOT EXISTS CATEGORY(
    CATEGORY_ID INT(11) NOT NULL PRIMARY KEY AUTO_INCREMENT,
    CATEGORY_NAME VARCHAR(50) NOT NULL,
    CATEGORY_TYPE VARCHAR(100) NOT NULL,
    CATEGORY_DESC VARCHAR(5000) NOT NULL
);
